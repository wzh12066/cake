package com.practice.webapp.entity;

public class Cake {
	
    private String cakeName;
    private double cakeCost;
    private double cakePrice;
    private int pruductQuantity;
    
    
	public String getCakeName() {
		return cakeName;
	}
	public void setCakeName(String cakeName) {
		this.cakeName = cakeName;
	}
	public double getCakeCost() {
		return cakeCost;
	}
	public void setCakeCost(double cakeCost) {
		this.cakeCost = cakeCost;
	}
	public double getCakePrice() {
		return cakePrice;
	}
	public void setCakePrice(double cakePrice) {
		this.cakePrice = cakePrice;
	}
	public int getPruductQuantity() {
		return pruductQuantity;
	}
	public void setPruductQuantity(int pruductQuantity) {
		this.pruductQuantity = pruductQuantity;
	}
    
	
	
	
}//Account